export const convertMonthly = (type) => {
  switch (type) {
    case 'T1':
      return 1;
    case 'T2':
      return 2;
    case 'T3':
      return 3;
    case 'T4':
      return 4;
    case 'T5':
      return 5;
    case 'T6':
      return 6;
    case 'T7':
      return 7;
    case 'T8':
      return 8;
    case 'T9':
      return 9;
    case 'T10':
      return 10;
    case 'T11':
      return 11;
    case 'T12':
      return 12;
  }
};
